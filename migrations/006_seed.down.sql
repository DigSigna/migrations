WHERE id IN (
    UUID_TO_BIN('USR00000-0000-0000-0000-000000000001'),
    UUID_TO_BIN('USR00000-0000-0000-0000-000000000002'),
    UUID_TO_BIN('USR00000-0000-0000-0000-000000000003')
);

